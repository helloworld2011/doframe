package frame.tableinfo;

public class OnetoMany {

	private String onename;
	private String oneclass;
	private String onecolumn;
	public String getOnename() {
		return onename;
	}
	public void setOnename(String onename) {
		this.onename = onename;
	}
	public String getOneclass() {
		return oneclass;
	}
	public void setOneclass(String oneclass) {
		this.oneclass = oneclass;
	}
	public String getOnecolumn() {
		return onecolumn;
	}
	public void setOnecolumn(String onecolumn) {
		this.onecolumn = onecolumn;
	}
}
